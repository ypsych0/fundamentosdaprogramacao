# 1. operadores matematicos

# + soma
# - subtracao
# * multiplicao
# / divisao
# ** potencia
# % modulo

numero_um = 20
numero_dois = 15

soma = numero_um + numero_dois
print(f"A soma entre {numero_um} + {numero_dois} e {soma}")

subtracao = numero_um - numero_dois
print(f"A subtracao entre {numero_um} - {numero_dois} e {subtracao}")

multiplicao = numero_um * numero_dois
print(f"A multiplicao entre {numero_um} * {numero_dois} e {multiplicao}")

divisao = numero_um / numero_dois
print(f"A divisao entre {numero_um} / {numero_dois} e {divisao}")

potencia = numero_um ** numero_dois
print(f"A potencia entre {numero_um} ** {numero_dois} e {potencia}")                               
