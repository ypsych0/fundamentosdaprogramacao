# Desenvolva um sistema de pizzaria onde será recebido o preço total, um desconto de 10%, e ao final exiba o valor total do pedido com esse desconto;

#porcentagem
# 100% = 1
# 50% = 0.5
# 25% = 0.25
# 18% = 0.18

preco1 = 45.90 #HardCode
desconto = preco1 * 0.10 #10%
total_do_pedido1 = preco1 - desconto
print(f"Total: R$ {total_do_pedido1:.2f}")

preco2 = 38.90 #HardCode
desconto2 = preco2 * 0.10 #10%
total_do_pedido2 = preco2 - desconto2
print(f"Total: R$ {total_do_pedido2:.2f}")


